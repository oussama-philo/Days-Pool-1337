#include <unistd.h>

int	ft_iterative_power(int nb, int power)
{
	int	rs;
	int	i;

	rs = 1;
	i = 0;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (i < power)
	{
		rs = rs * nb;
		i++;
	}
	return (rs);
}
