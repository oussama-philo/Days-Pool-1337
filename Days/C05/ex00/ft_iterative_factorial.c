int	ft_iterative_factorial(int nb)
{
	int	rs;
	int	i;

	i = 1;
	rs = 1;
	if (nb == 0)
		return (1);
	while (i <= nb)
	{
		rs = rs * i;
		i++;
	}
	return (rs);
}
