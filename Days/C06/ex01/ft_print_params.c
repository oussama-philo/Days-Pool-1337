#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	*arg;

	i = 1;
	while (i < ac)
	{
		arg = av[i];
		while (*arg != '\0')
		{
			write(1, arg, 1);
			arg++;
		}
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
